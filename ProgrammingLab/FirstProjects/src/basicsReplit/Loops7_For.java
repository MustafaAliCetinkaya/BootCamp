package basicsReplit;

public class Loops7_For {
    public static void main(String[] args) {

        String [] array={"ayşe", "fatma", "hayriye", "haydi", "çifte", "telliye", "başı", "dönmez", "elliye"};

        for (int i=0;i<array.length;i++){
            System.out.println(array[i].toUpperCase());
        }

    }
}
