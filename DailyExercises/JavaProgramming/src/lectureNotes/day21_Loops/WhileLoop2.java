package day21_Loops;

public class WhileLoop2 {

    public static void main(String[] args) {

        for (int i = 0; i < 3; i++) {
            System.out.println("Hello Cybertek");
        }

        System.out.println("---------------------------");

        int j = 0;
        while( j < 3){
            System.out.println("Hello Cybertek");
            j++;
        }



    }

}
